    private void jButtonSupprimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSupprimActionPerformed
        if (jTableVente.getSelectedRow() == -1)
            JOptionPane.showMessageDialog(null,"Sélectionnez d'abord une ligne svp !","Avertissement",JOptionPane.ERROR_MESSAGE);
        else {
            int rep = JOptionPane.showConfirmDialog(this,"Voulez-vous vraiment supprimer " +
                    myVenModel.getValueAt(jTableVente.convertRowIndexToModel(jTableVente.getSelectedRow()), 0) + " ?", "Confirmation", JOptionPane.YES_NO_OPTION);
            if (rep == JOptionPane.YES_OPTION)
                try {    
                    daoVen.deleteVentes((Integer)myVenModel.getValueAt(jTableVente.convertRowIndexToModel(jTableVente.getSelectedRow()), 0));
                }
                catch (DaoException e) {
                    JOptionPane.showMessageDialog(null,"Suppression impossible !","Avertissement",JOptionPane.ERROR_MESSAGE);
                }
        }
        // après la suppression : rafraîchir les données en relisant la BD
        myVenModel.setMyList(daoVen.selectVentes());
    }//GEN-LAST:event_jButtonSupprimActionPerformed

