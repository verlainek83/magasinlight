    private void fillComponents() throws DaoException
    {
        /* charger toutes les clients */

        ArrayList<Clients> listeCli = daoCli.selectClients();
        listeCli.add(0, new Clients(-1,"*")); // ajoute une cat bidon : -1 signifie toutes les cat
        for (Clients cli : listeCli) {
            jComboBoxCli.addItem(cli); // on insère des objets dans les combo, mais ils sont affichés grâce à leurs méthodes toString() !
        }
        
        jComboBoxCli.setEnabled(false);
        jRadioButtonAucun.setSelected(true);
     }
    
