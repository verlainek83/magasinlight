    private void jButtonModifActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModifActionPerformed
        if (jTableVente.getSelectedRow() == -1)
            JOptionPane.showMessageDialog(this,"Sélectionnez d'abord une ligne svp !","Avertissement",JOptionPane.ERROR_MESSAGE);
        else {
            // renvoie l'objet Ventes à passer en paramètre
            Ventes ven = myVenModel.getMyList(jTableVente.convertRowIndexToModel(jTableVente.getSelectedRow()));
                      
            // récupère la JFrame parente
            JFrame parent = (JFrame)(this.getDesktopPane().getTopLevelAncestor());
            new JDVentesModif(parent, "MODIFICATION De la vente", ven);
            
            // après la modification : rafraîchir les données en relisant la BD
            myVenModel.setMyList(daoVen.selectVentes()); 
        }
    }//GEN-LAST:event_jButtonModifActionPerformed

